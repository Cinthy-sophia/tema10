package com.cinthyasophia.tema10.Ejercicio10;

public class Hijo {
    private String nombre;
    private String apellido;
    private int edad;

    public Hijo(String nombre, String apellido, int edad){
        this.nombre=nombre;
        this.apellido=apellido;
        this.edad=edad;
    }
    public Hijo(){
        nombre=" ";
        apellido= " ";
        edad=0;
    }

}
